  Widget getButtonUI(CategoryType categoryTypeData, bool isSelected) {
    String txt = '';
    if (CategoryType.father == categoryTypeData) {
      txt = 'Hadi';
    } else if (CategoryType.mother == categoryTypeData) {
      txt = 'Diah';
